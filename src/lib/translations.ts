// Helper function to get translation
export const t = (key: string, lang: Language = 'en'): string => {
  const keys = key.split('.');
  let value: any = translations;
  
  for (const k of keys) {
    value = value?.[k];
    if (!value) return key;
  }
  
  return value[lang] || value['en'] || key;
};
